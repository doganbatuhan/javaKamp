package day2Hw1;

public class Course {

	int id;
	String name;
	String instructorName;
	double completionRate;
	
	public Course(int id, String name, String instructorName, double completionRate) {
		this.id = id;
		this.name = name;
		this.instructorName = instructorName;
		this.completionRate = completionRate;
	}
}
